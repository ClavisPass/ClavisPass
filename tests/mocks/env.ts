export let DROPBOX_CLIENT_ID = "dropbox-client-id";
export let GOOGLE_CLIENT_ID = "";
export let GOOGLE_CLIENT_ID_ANDROID = "";
export let GOOGLE_CLIENT_ID_IOS = "";
export let GOOGLE_CLIENT_ID_DESKTOP = "";
export let GOOGLE_CLIENT_SECRET_DESKTOP = "";

export function setEnv(next: {
  DROPBOX_CLIENT_ID?: string;
  GOOGLE_CLIENT_ID?: string;
  GOOGLE_CLIENT_ID_ANDROID?: string;
  GOOGLE_CLIENT_ID_IOS?: string;
  GOOGLE_CLIENT_ID_DESKTOP?: string;
  GOOGLE_CLIENT_SECRET_DESKTOP?: string;
}) {
  DROPBOX_CLIENT_ID = next.DROPBOX_CLIENT_ID ?? DROPBOX_CLIENT_ID;
  GOOGLE_CLIENT_ID = next.GOOGLE_CLIENT_ID ?? GOOGLE_CLIENT_ID;
  GOOGLE_CLIENT_ID_ANDROID =
    next.GOOGLE_CLIENT_ID_ANDROID ?? GOOGLE_CLIENT_ID_ANDROID;
  GOOGLE_CLIENT_ID_IOS = next.GOOGLE_CLIENT_ID_IOS ?? GOOGLE_CLIENT_ID_IOS;
  GOOGLE_CLIENT_ID_DESKTOP =
    next.GOOGLE_CLIENT_ID_DESKTOP ?? GOOGLE_CLIENT_ID_DESKTOP;
  GOOGLE_CLIENT_SECRET_DESKTOP =
    next.GOOGLE_CLIENT_SECRET_DESKTOP ?? GOOGLE_CLIENT_SECRET_DESKTOP;
}

export function resetEnv() {
  DROPBOX_CLIENT_ID = "dropbox-client-id";
  GOOGLE_CLIENT_ID = "";
  GOOGLE_CLIENT_ID_ANDROID = "";
  GOOGLE_CLIENT_ID_IOS = "";
  GOOGLE_CLIENT_ID_DESKTOP = "";
  GOOGLE_CLIENT_SECRET_DESKTOP = "";
}
